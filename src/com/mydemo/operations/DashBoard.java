    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
      int train_no= Integer.valueOf((String)trainList.getSelectedItem());
        System.out.println("book ticket "+trainList.getSelectedItem());
        boolean isSeatsAvailable;
        TrainSeatImp trainImp = new TrainSeatImp();
        TrainSeat trainSeat = new TrainSeat();
        trainSeat.setNoOfSeats(Integer.valueOf(noOfPassenger.getText().trim()));
        trainSeat.setTrainNo(train_no);
        System.out.println("train no "+trainSeat.getTrainNo());
        isSeatsAvailable = trainImp.checkSeats(trainSeat);
        if (isSeatsAvailable) {
            //train fare calculating
            TrainfareCalculator trainfareCalculator = new TrainfareCalculator();
            trainfareCalculator.setSource(route.getSrc());
            trainfareCalculator.setDestination(route.getDesc());
            trainfareCalculator.setTrainNo(train_no);
            trainfareCalculator.setNoOfPassenger(Integer.valueOf(noOfPassenger.getText().trim()));
            float totalCost = new TrainImp().calculateCost(trainfareCalculator);
            //transaction part 
            TransactionImp imp = new TransactionImp();
            Transaction transaction = new Transaction();
            transaction.setCustomerName(customerName.getText().trim());
            transaction.setTrainNo(train_no);
            transaction.setTotalCost(totalCost);
            transaction.setNoOfPassenger(Integer.valueOf(noOfPassenger.getText()));
            imp.initTransaction(transaction);
        } else {
            System.out.println("tickets not available");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

