    private void getTrainActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_getTrainActionPerformed
        
        trainList.removeAllItems();
        
        String src = source.getText();
        String desc = destination.getText();
         route = new Routes();
        route.setSrc(src);
        route.setDesc(desc);
        RoutesImp routesImp = new RoutesImp();
        route = routesImp.getAllTrainDetails(route);
        defaultModel = new DefaultListModel();
        route.getTrainList().forEach((trainno) -> {
            trainList.addItem("" + trainno);
            defaultModel.addElement(trainno);
            
        });
      


    }//GEN-LAST:event_getTrainActionPerformed

